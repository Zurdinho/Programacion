public class ConcatenandoStrings{
	
	public static void main(String args[]) {
		
	String saludo, nombre;
	
	saludo = "Buenos Dias";
	nombre = "Fernando";

	//forma uno
	System.out.println(saludo + " " + nombre);

	//forma dos
	System.out.print(saludo);
	System.out.print(" ");
	System.out.println(nombre);
	}
}